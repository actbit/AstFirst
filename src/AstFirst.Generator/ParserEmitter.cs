using System.Collections.Generic;
using System.Text;
using AstFirst.Core.Lexing;
using AstFirst.Core.Parsing;

namespace AstFirst.Generator;

/// <summary>
/// GrammarModel からパーサの C# コードを生成する。
/// LalrTable (ACTION/GOTO/Alternatives) と Productions を static 配列に直列化し、
/// shift/reduce/accept を駆動する。スタックは配列 + top 指数 (再確保を削減)。
/// reduce は「仮想 reduce」: top から右辺長分を Pop せず参照し、partial コンストラクタ new →
/// OnReduce(ctx) → Accept/Reject を判定。Reject なら Alternatives の次候補へフォールバック。
/// Parse 後に 2パス目 (OnSecondPassEnter/Exit) を自動呼出。
/// </summary>
public static class ParserEmitter
{
    public static string EmitParser(GrammarModel model, Grammar grammar, LalrTable table, IReadOnlyList<LexerRule> rules, string ns)
    {
        int stateCount = table.StateCount;
        int symbolCount = table.SymbolCount;
        int prodCount = grammar.Productions.Count;
        var (typeName, lexerName, parserName) = Names(model);

        // 終端 Symbol.Id と LexerRule.TokenId を突き合わせ。
        var patternToTerminalId = new Dictionary<string, int>();
        foreach (var sym in grammar.Symbols)
        {
            if (!sym.IsTerminal) continue;
            if (sym.Name.StartsWith("token:"))
                patternToTerminalId[sym.Name.Substring("token:".Length)] = sym.Id;
        }

        // Token 派生型。reduce 時、BasicToken.Text から派生型を再生成。
        var tokenDerivedTypes = new HashSet<string>();
        foreach (var td in model.TokenDefs)
            if (td.Key != "AstFirst.Token") tokenDerivedTypes.Add(td.Key);
        int maxTokenId = 0;
        foreach (var r in rules) if (r.TokenId > maxTokenId) maxTokenId = r.TokenId;
        var tokenIdToSym = new int[maxTokenId + 1];
        for (int i = 0; i < tokenIdToSym.Length; i++) tokenIdToSym[i] = -1;
        foreach (var r in rules)
            if (patternToTerminalId.TryGetValue(r.Pattern, out var sid))
                tokenIdToSym[r.TokenId] = sid;

        int eofSym = grammar.EndOfFile.Id;

        // コンフリクトセルのフォールバック候補を収集 (候補2以上のセルのみ)。
        var altKeys = new List<int>();
        var altActs = new List<List<int>>();
        for (int s = 0; s < stateCount; s++)
            for (int c = 0; c < symbolCount; c++)
            {
                var alts = table.Alternatives(s, c);
                if (alts.Count < 2) continue;
                // デフォルト (先頭) は Action(s,c) と同じはず。敗者候補 (2番目以降) を記録。
                var list = new List<int>();
                for (int k = 1; k < alts.Count; k++) list.Add(EncodeAction(alts[k]));
                if (list.Count == 0) continue;
                altKeys.Add(s * symbolCount + c);
                altActs.Add(list);
            }

        var sb = new StringBuilder();
        sb.AppendLine("// <auto-generated/>");
        sb.AppendLine("#nullable enable");
        sb.AppendLine("using System;");
        sb.AppendLine("using System.Collections.Generic;");
        sb.AppendLine("using AstFirst.Core.Lexing;");
        if (ns.Length > 0) { sb.AppendLine("namespace " + ns + ";"); sb.AppendLine(); }
        sb.AppendLine("public static class " + parserName);
        sb.AppendLine("{");
        // 2パス目 (Walker) を使う文法でのみ、既定 Walker インスタンスを保持 (ゼロコスト)。
        if (model.HasSecondPass)
        {
            var walkerSuffix = string.IsNullOrEmpty(model.Mode) ? "" : "_" + model.Mode;
            var (_, rootType) = CodeEmitter.SplitFullName(model.RootTypeFullName);
            var walkerName = rootType + walkerSuffix + "Walker";
            sb.AppendLine("    private static readonly " + walkerName + " __defaultWalker = new " + walkerName + "._Default();");
        }

        EmitMatrixByte(sb, "ActionKind", table, stateCount, symbolCount, s => c => ActionKindByte(table.Action(s, c)));
        EmitMatrixIntRaw(sb, "ActionValue", stateCount, symbolCount, s => c => table.Action(s, c).Value);
        EmitMatrixIntRaw(sb, "Goto", stateCount, symbolCount, s => c => table.Goto(s, c));

        sb.Append("    public static readonly int[] ProdLhs = new int[] { ");
        for (int p = 0; p < prodCount; p++) { if (p > 0) sb.Append(", "); sb.Append(grammar.Productions[p].Lhs.Id); }
        sb.AppendLine(" };");
        sb.Append("    public static readonly int[] ProdLen = new int[] { ");
        for (int p = 0; p < prodCount; p++) { if (p > 0) sb.Append(", "); sb.Append(grammar.Productions[p].Rhs.Length); }
        sb.AppendLine(" };");

        sb.Append("    public static readonly int[] TokenIdToSym = new int[] { ");
        for (int i = 0; i < tokenIdToSym.Length; i++) { if (i > 0) sb.Append(", "); sb.Append(tokenIdToSym[i]); }
        sb.AppendLine(" };");

        sb.AppendLine("    public const int EofSym = " + eofSym + ";");
        sb.AppendLine("    public const int StateCount = " + stateCount + ";");
        sb.AppendLine("    public const int SymbolCount = " + symbolCount + ";");

        sb.Append("    public static readonly string?[] SymNames = new string?[] { ");
        for (int si = 0; si < symbolCount; si++)
        {
            if (si > 0) sb.Append(", ");
            var escaped = grammar.Symbols[si].Name.Replace("\\", "\\\\").Replace("\"", "\\\"");
            sb.Append("\"").Append(escaped).Append("\"");
        }
        sb.AppendLine(" };");

        var dr = table.DefaultReduceTable;
        sb.Append("    public static readonly int[] DefaultReduce = new int[] { ");
        for (int s = 0; s < stateCount; s++) { if (s > 0) sb.Append(", "); sb.Append(s < dr.Count ? dr[s] : -1); }
        sb.AppendLine(" };");

        // フォールバック候補 (コンフリクトセル)
        sb.Append("    private static readonly int[] AltKeys = new int[] { ");
        for (int i = 0; i < altKeys.Count; i++) { if (i > 0) sb.Append(", "); sb.Append(altKeys[i]); }
        sb.AppendLine(" };");
        sb.AppendLine("    private static readonly int[][] AltActs = new int[" + altActs.Count + "][]");
        sb.AppendLine("    {");
        for (int i = 0; i < altActs.Count; i++)
        {
            sb.Append("        new int[] { ");
            for (int j = 0; j < altActs[i].Count; j++) { if (j > 0) sb.Append(", "); sb.Append(altActs[i][j]); }
            sb.Append(" }");
            sb.AppendLine(i < altActs.Count - 1 ? "," : "");
        }
        sb.AppendLine("    };");

        EmitParse(sb, lexerName, model);
        EmitHelpers(sb, grammar, model, tokenDerivedTypes);

        sb.AppendLine("}");
        return sb.ToString();
    }

    private static void EmitParse(StringBuilder sb, string lexerName, GrammarModel model)
    {
        sb.AppendLine("    public static AstFirst.ParseResult Parse(string input) => Parse(input, null);");
        sb.AppendLine("    public static AstFirst.ParseResult Parse(string input, AstFirst.SemanticContext? context)");
        sb.AppendLine("    {");
        sb.AppendLine("        var ctx = context ?? new AstFirst.BasicSemanticContext();");
        sb.AppendLine("        var tokens = " + lexerName + ".Tokenize(input);");
        sb.AppendLine("        var states = new int[64];");
        sb.AppendLine("        var values = new object?[64];");
        sb.AppendLine("        Span<int> statesSpan = states;");
        sb.AppendLine("        Span<object?> valuesSpan = values;");
        sb.AppendLine("        int top = 0;");
        sb.AppendLine("        var errors = new System.Collections.Generic.List<AstFirst.ParseError>();");
        sb.AppendLine("        object? result = null;");
        sb.AppendLine("        statesSpan[top++] = 0;");
        sb.AppendLine("        int i = 0;");
        sb.AppendLine("        int lastErrorPos = -10;");
        sb.AppendLine("        while (true)");
        sb.AppendLine("        {");
        sb.AppendLine("            int state = statesSpan[top - 1];");
        sb.AppendLine("            int sym = (i < tokens.Count) ? TokenIdToSym[tokens[i].TokenId] : EofSym;");
        sb.AppendLine("            if (sym < 0) { errors.Add(new AstFirst.ParseError(\"未知のトークンです\", tokens[i].Start)); i++; continue; }");
        sb.AppendLine("            byte kind = ActionKind[state * SymbolCount + sym];");
        sb.AppendLine("            int val = ActionValue[state * SymbolCount + sym];");
        sb.AppendLine("            int dr = DefaultReduce[state];");
        sb.AppendLine("            // デフォルトアクション決定 (テーブル圧縮のデフォルト reduce を展開)");
        sb.AppendLine("            byte dk = kind; int dv = val;");
        sb.AppendLine("            if (kind == 0 && dr >= 0) { dk = 2; dv = dr; }");
        sb.AppendLine("");
        sb.AppendLine("            if (dk == 1) // Shift");
        sb.AppendLine("            {");
        sb.AppendLine("                if (top >= statesSpan.Length) { Grow(ref states, ref values); statesSpan = states; valuesSpan = values; }");
        sb.AppendLine("                valuesSpan[top] = sym == EofSym ? null : (object)ToToken(tokens[i]);");
        sb.AppendLine("                statesSpan[top] = dv; top++;");
        sb.AppendLine("                i++;");
        sb.AppendLine("            }");
        sb.AppendLine("            else if (dk == 2) // Reduce (仮想 reduce)");
        sb.AppendLine("            {");
        sb.AppendLine("                var node = ReduceNode(dv, valuesSpan, top, ctx);");
        sb.AppendLine("                if (node is AstFirst.AstNode __an && !__an.IsAccepted)");
        sb.AppendLine("                {");
        sb.AppendLine("                    // Reject: フォールバック候補を試す");
        sb.AppendLine("                    if (!TryFallback(state, sym, ref values, ref top, ref states, ctx, tokens, ref i)) goto panic;");
        sb.AppendLine("                    valuesSpan = values; statesSpan = states;");
        sb.AppendLine("                }");
        sb.AppendLine("                else");
        sb.AppendLine("                {");
        sb.AppendLine("                    int len = ProdLen[dv];");
        sb.AppendLine("                    top -= len;");
        sb.AppendLine("                    valuesSpan[top] = node;");
        sb.AppendLine("                    statesSpan[top] = Goto[statesSpan[top - 1] * SymbolCount + ProdLhs[dv]]; top++;");
        sb.AppendLine("                }");
        sb.AppendLine("            }");
        sb.AppendLine("            else if (dk == 3) // Accept");
        sb.AppendLine("            {");
        sb.AppendLine("                top--; // $ を捨てる");
        sb.AppendLine("                result = valuesSpan[top - 1];");
        sb.AppendLine("                break;");
        sb.AppendLine("            }");
        sb.AppendLine("            else { goto panic; }");
        sb.AppendLine("            continue;");
        sb.AppendLine("        panic:");
        sb.AppendLine("            {");
        sb.AppendLine("                int pos = i < tokens.Count ? tokens[i].Start : (tokens.Count > 0 ? tokens[tokens.Count - 1].End : 0);");
        sb.AppendLine("                if (i - lastErrorPos >= 3)");
        sb.AppendLine("                {");
        sb.AppendLine("                    var exp = new System.Text.StringBuilder();");
        sb.AppendLine("                    for (int e = 0; e < SymbolCount; e++)");
        sb.AppendLine("                    {");
        sb.AppendLine("                        if (ActionKind[state * SymbolCount + e] == 0) continue;");
        sb.AppendLine("                        if (exp.Length > 0) exp.Append(\", \");");
        sb.AppendLine("                        exp.Append(e == EofSym ? \"EOF\" : e < SymNames.Length ? SymNames[e] : \"#\" + e);");
        sb.AppendLine("                    }");
        sb.AppendLine("                    errors.Add(new AstFirst.ParseError(\"予期しないトークン\" + (exp.Length > 0 ? \" (期待: \" + exp + \")\" : \"\"), pos));");
        sb.AppendLine("                    lastErrorPos = i;");
        sb.AppendLine("                }");
        sb.AppendLine("                var __et = new AstFirst.Glr.GlrTables(ActionKind, ActionValue, Goto, ProdLhs, ProdLen, DefaultReduce, TokenIdToSym, AltKeys, AltActs, StateCount, SymbolCount, EofSym, 0, SymNames);");
        sb.AppendLine("                var __es = new AstFirst.Glr.LightGlrDriver.LightGlrStack(states, values, top, i);");
        sb.AppendLine("                var __er = AstFirst.Glr.ErrorRepair.TryRepair(__et, tokens, __es, (int p, object?[] c, AstFirst.SemanticContext x) => ReduceNode(p, c.AsSpan(), c.Length, x), ToToken, ctx);");
        sb.AppendLine("                if (__er != null) { states = __er.States; values = __er.Values; top = __er.Top; i = __er.Pos; statesSpan = states; valuesSpan = values; continue; }");
        sb.AppendLine("                bool recovered = false;");
        sb.AppendLine("                while (top > 1 && i < tokens.Count)");
        sb.AppendLine("                {");
        sb.AppendLine("                    top--;");
        sb.AppendLine("                    int topState = statesSpan[top - 1];");
        sb.AppendLine("                    int curSym = TokenIdToSym[tokens[i].TokenId];");
        sb.AppendLine("                    if (curSym >= 0 && ActionKind[topState * SymbolCount + curSym] != 0) { recovered = true; break; }");
        sb.AppendLine("                }");
        sb.AppendLine("                if (!recovered)");
        sb.AppendLine("                {");
        sb.AppendLine("                    if (top <= 1) { top = 0; statesSpan[top++] = 0; }");
        sb.AppendLine("                    if (i < tokens.Count) i++; else break;");
        sb.AppendLine("                }");
        sb.AppendLine("            }");
        sb.AppendLine("        }");
        // 2パス目: IOnSecondPassEnter/Exit を実装するノードが1つでもあれば Walker を生成・呼出。
        // 未実装文法では走査ごと省略 (空呼びによる Parse 時間の無駄を回避)。
        if (model.HasSecondPass)
            sb.AppendLine("        if (result is AstFirst.AstNode root) __defaultWalker.Walk(root, ctx);");
        sb.AppendLine("        return new AstFirst.ParseResult(result, errors, ctx.Diagnostics.Items);");
        sb.AppendLine("    }");
    }

    private static void EmitHelpers(StringBuilder sb, Grammar grammar, GrammarModel model, HashSet<string> tokenDerivedTypes)
    {
        // Grow: スタック配列が足りなくなったら 2 倍に拡張。
        sb.AppendLine("    private static void Grow(ref int[] states, ref object?[] values)");
        sb.AppendLine("    {");
        sb.AppendLine("        System.Array.Resize(ref states, states.Length * 2);");
        sb.AppendLine("        System.Array.Resize(ref values, values.Length * 2);");
        sb.AppendLine("    }");

        // TryFallback: Reject された reduce の代わりにコンフリクト候補を試す。
        sb.AppendLine("    private static bool TryFallback(int state, int sym, ref object?[] values, ref int top, ref int[] states, AstFirst.SemanticContext ctx, System.Collections.Generic.List<AstFirst.Core.Lexing.LexToken> tokens, ref int i)");
        sb.AppendLine("    {");
        sb.AppendLine("        int key = state * SymbolCount + sym;");
        sb.AppendLine("        for (int a = 0; a < AltKeys.Length; a++)");
        sb.AppendLine("        {");
        sb.AppendLine("            if (AltKeys[a] != key) continue;");
        sb.AppendLine("            foreach (var e in AltActs[a])");
        sb.AppendLine("            {");
        sb.AppendLine("                int k = e / 1000000; int v = e % 1000000;");
        sb.AppendLine("                if (k == 1) // Shift 候補");
        sb.AppendLine("                {");
        sb.AppendLine("                    if (top >= states.Length) Grow(ref states, ref values);");
        sb.AppendLine("                    values[top] = sym == EofSym ? null : (object)ToToken(tokens[i]);");
        sb.AppendLine("                    states[top] = v; top++;");
        sb.AppendLine("                    i++;");
        sb.AppendLine("                    return true;");
        sb.AppendLine("                }");
        sb.AppendLine("                if (k == 2) // Reduce 候補");
        sb.AppendLine("                {");
        sb.AppendLine("                    var node = ReduceNode(v, values, top, ctx);");
        sb.AppendLine("                    if (node is AstFirst.AstNode __an && !__an.IsAccepted) continue; // さらに Reject → 次");
        sb.AppendLine("                    int len = ProdLen[v];");
        sb.AppendLine("                    top -= len;");
        sb.AppendLine("                    values[top] = node;");
        sb.AppendLine("                    states[top] = Goto[states[top - 1] * SymbolCount + ProdLhs[v]]; top++;");
        sb.AppendLine("                    return true;");
        sb.AppendLine("                }");
        sb.AppendLine("            }");
        sb.AppendLine("            return false;");
        sb.AppendLine("        }");
        sb.AppendLine("        return false;");
        sb.AppendLine("    }");

        // ReduceNode: 規則 prodId で仮想 reduce。スタック top から右辺長分を Pop せず参照 → partial コンストラクタ new → OnReduce(ctx)。
        sb.AppendLine("    private static object? ReduceNode(int val, Span<object?> values, int top, AstFirst.SemanticContext ctx)");
        sb.AppendLine("    {");
        sb.AppendLine("        switch (val)");
        sb.AppendLine("        {");
        foreach (var prod in grammar.Productions)
        {
            switch (prod.Tag)
            {
                case ReduceActionModel action:
                {
                    int len = prod.Rhs.Length;
                    sb.Append("            case ").Append(prod.Id).Append(": { return new ").Append(action.AstTypeName).Append("(\"").Append(action.RuleName).Append("\"");
                    for (int j = 0; j < action.Parameters.Count; j++)
                    {
                        sb.Append(", ");
                        var p = action.Parameters[j];
                        // 子は values[top - len + ChildIndex] で参照 (Pop せず)。右辺の ChildIndex 番目の記号。
                        if (p.IsContext) sb.Append("(").Append(p.CastTypeName).Append(")ctx");
                        else if (tokenDerivedTypes.Contains(p.CastTypeName)) sb.Append("new ").Append(p.CastTypeName).Append("(((AstFirst.Token)values[top - ").Append(len).Append(" + ").Append(p.ChildIndex).Append("]!).Text)");
                        else sb.Append("(").Append(p.CastTypeName).Append(")values[top - ").Append(len).Append(" + ").Append(p.ChildIndex).Append("]!");
                    }
                    sb.AppendLine("); }");
                    break;
                }
                case ListReduceActionModel listAction:
                {
                    int len = prod.Rhs.Length;
                    string elemType = listAction.ElementType;
                    string listType = "System.Collections.Generic.List<" + elemType + ">";
                    sb.Append("            case ").Append(prod.Id).Append(": { ");
                    if (listAction.IsRecursive)
                    {
                        // List_T → List_T item: 既存リスト (右辺0) に item (右辺1) を Add。
                        sb.Append("var __list = (").Append(listType).Append(")values[top - ").Append(len).Append(" + 0]!; ");
                        sb.Append("__list.Add((").Append(elemType).Append(")values[top - ").Append(len).Append(" + 1]!); ");
                    }
                    else if (listAction.IsEmpty)
                    {
                        // List_T → ε: 空リスト (Star のみ)。
                        sb.Append("var __list = new ").Append(listType).Append("(0); ");
                    }
                    else
                    {
                        // List_T → item: 1要素の新規リスト。
                        sb.Append("var __list = new ").Append(listType).Append("(4); ");
                        sb.Append("__list.Add((").Append(elemType).Append(")values[top - ").Append(len).Append(" + 0]!); ");
                    }
                    sb.AppendLine("return __list; }");
                    break;
                }
                case PassThroughActionModel:
                {
                    int len = prod.Rhs.Length;
                    // 抽象クラス経由の単位規則 (Base → N): 右辺 N の値 (具象サブクラスのインスタンス) をそのまま返す。
                    sb.Append("            case ").Append(prod.Id).Append(": { return values[top - ").Append(len).Append(" + 0]!; }").AppendLine();
                    break;
                }
            }
        }
        sb.AppendLine("            default: return null;");
        sb.AppendLine("        }");
        sb.AppendLine("    }");

        sb.AppendLine("    private static AstFirst.Token ToToken(AstFirst.Core.Lexing.LexToken t)");
        sb.AppendLine("        => new AstFirst.BasicToken(t.Span, new AstFirst.SourceSpan(new AstFirst.Position(t.Start, t.StartLine, t.StartColumn), new AstFirst.Position(t.End, t.EndLine, t.EndColumn)));");
    }

    /// <summary>各具象ノードの partial コード (子/終端 readonly フィールド + RuleName + OnReduce 宣言 + 各[Rule]の partial コンストラクタ) を生成。</summary>
    public static string EmitPartial(GrammarModel model, NodeModel node, string ns)
    {
        var (nodeNs, simple) = CodeEmitter.SplitFullName(node.FullName);
        var sb = new StringBuilder();
        sb.AppendLine("// <auto-generated/>");
        sb.AppendLine("#nullable enable");
        if (nodeNs.Length > 0) { sb.AppendLine("namespace " + nodeNs + ";"); sb.AppendLine(); }

        string? ctxType = CtxTypeOf(node);
        string? ctxArg = CtxArgOf(node);
        string ctxParam = ctxType is not null ? "(" + ctxType + " " + (ctxArg ?? "ctx") + ")" : "()";
        string ctxCall = ctxType is not null ? (ctxArg ?? "ctx") : "";

        sb.AppendLine("public partial class " + simple);
        sb.AppendLine("{");
        // 中間抽象のプロパティ継承: 抽象基底 ([Rule] でプロパティ宣言) は全プロパティを定義し、
        // 具象サブクラスは継承プロパティを再定義せず : base(...) で初期化する (readonly 維持)。
        bool isAbstractBase = node.IsAbstract && node.Rules.Count > 0;
        var inheritedSet = new HashSet<string>(node.InheritedPropertyNames);
        bool hasInherited = inheritedSet.Count > 0;

        // 子 (AstNode 派生) + 終端 (Token): readonly フィールド (全[Rule]の引数から統合、PascalCase)。
        // 具象クラスは継承プロパティ (基底で定義済み) を再定義しない。
        var propSet = new HashSet<string>();
        foreach (var rule in node.Rules)
            foreach (var p in rule.Parameters)
            {
                if (p.IsContext || p.Name is null) continue;
                var prop = CodeEmitter.Pascalize(p.Name);
                if (!propSet.Add(prop)) continue;
                if (!isAbstractBase && inheritedSet.Contains(prop)) continue;
                var typeStr = p.IsRepeat
                    ? "System.Collections.Generic.IReadOnlyList<" + p.TypeFullName + ">"
                    : p.TypeFullName;
                sb.AppendLine("    public readonly " + typeStr + " " + prop + ";");
            }
        // RuleName: 抽象基底、または継承プロパティがない (基底が RuleName を持たない) 場合のみ生成。
        if (node.Rules.Count > 0 && (isAbstractBase || !hasInherited))
            sb.AppendLine("    public readonly string RuleName;");
        sb.AppendLine("    partial void OnReduce" + ctxParam + ";");

        // コンストラクタ: 抽象基底は protected (派生から : base で呼ばれる)、具象は internal。
        // 同じ引数型シグネチャの[Rule]が複数ある場合は1つに統合 (ruleName で実行時に区別)。
        string ctorKind = isAbstractBase ? "protected" : "internal";
        var seenSigs = new HashSet<string>();
        foreach (var rule in node.Rules)
        {
            var sigBuilder = new StringBuilder();
            foreach (var p in rule.Parameters) { if (sigBuilder.Length > 0) sigBuilder.Append(','); sigBuilder.Append(p.TypeFullName); }
            if (!seenSigs.Add(sigBuilder.ToString())) continue;

            sb.Append("    " + ctorKind + " " + simple + "(string ruleName");
            foreach (var p in rule.Parameters)
            {
                sb.Append(", ");
                var typeStr = p.IsRepeat
                    ? "System.Collections.Generic.IReadOnlyList<" + p.TypeFullName + ">"
                    : p.TypeFullName;
                sb.Append(typeStr).Append(" ").Append(p.Name ?? "p");
            }
            sb.AppendLine(")");

            // 継承ありの具象: : base(ruleName, 継承プロパティ...) を呼ぶ (基底のプロパティ順)。
            if (hasInherited && !isAbstractBase)
            {
                sb.Append("        : base(ruleName");
                foreach (var inheritedProp in node.InheritedPropertyNames)
                {
                    string? argName = null;
                    foreach (var p in rule.Parameters)
                        if (p.Name is not null && CodeEmitter.Pascalize(p.Name) == inheritedProp) { argName = p.Name; break; }
                    if (argName is not null) sb.Append(", ").Append(argName);
                }
                sb.AppendLine(")");
            }

            sb.AppendLine("    {");
            // RuleName 代入: 抽象基底、または継承なし (基底が持たない) の場合。
            if (isAbstractBase || !hasInherited)
                sb.AppendLine("        RuleName = ruleName;");
            foreach (var p in rule.Parameters)
                if (!p.IsContext && p.Name is not null)
                {
                    var prop = CodeEmitter.Pascalize(p.Name);
                    if (isAbstractBase || !inheritedSet.Contains(prop))
                        sb.AppendLine("        this." + prop + " = " + p.Name + ";");
                }
            // Span 自動計算: 子の Span を順次マージして this.Span に設定。
            // OnReduce の「前」に設定するのでユーザーが OnReduce で上書き可能 (後方互換)。
            // 抽象基底 (isAbstractBase) ではスキップ — 具象コンストラクタが継承子含む全子を持つため冗長。
            if (!isAbstractBase)
            {
                sb.AppendLine("        var __autoSpan = default(AstFirst.SourceSpan);");
                sb.AppendLine("        var __autoSpanHas = false;");
                foreach (var p in rule.Parameters)
                {
                    if (p.IsContext || p.Name is null) continue;
                    if (p.IsRepeat)
                    {
                        // [Repeat] 子: IReadOnlyList<T> (T は AstNode/Token 派生、いずれも .Span を持つ)。
                        sb.AppendLine("        foreach (var __autoSpanItem in " + p.Name
                            + ") { __autoSpan = __autoSpanHas ? AstFirst.SourceSpan.Merge(__autoSpan, __autoSpanItem.Span) : __autoSpanItem.Span; __autoSpanHas = true; }");
                    }
                    else
                    {
                        sb.AppendLine("        __autoSpan = __autoSpanHas ? AstFirst.SourceSpan.Merge(__autoSpan, "
                            + p.Name + ".Span) : " + p.Name + ".Span; __autoSpanHas = true;");
                    }
                }
                sb.AppendLine("        if (__autoSpanHas) Span = __autoSpan;");
            }
            sb.AppendLine("        OnReduce(" + ctxCall + ");");
            // [OnReduce] 属性付き意味解析ルール ([Grammar] ルートクラスの static メソッド)。partial OnReduce の直後に呼ぶ (共存)。
            foreach (var ar in model.AnalyzeRules)
            {
                if (ar.Phase != AnalyzePhase.OnReduce) continue;
                if (ar.TargetNodeFullName != node.FullName) continue;
                if (ctxCall.Length == 0) continue; // ctx なしノードには注入不可
                sb.AppendLine("        " + ar.GrammarClassFullName + "." + ar.MethodName + "(this, (" + ar.CtxTypeFullName + ")" + ctxCall + ");");
            }
            sb.AppendLine("    }");
        }
        sb.AppendLine("}");
        return sb.ToString();
    }

    /// <summary>ノードの [Rule] の最後の SemanticContext 派生引数の型。なければ null (ctx なし)。</summary>
    private static string? CtxTypeOf(NodeModel n)
    {
        if (n.Rules.Count == 0) return null;
        var ps = n.Rules[0].Parameters;
        for (int j = ps.Count - 1; j >= 0; j--)
            if (ps[j].IsContext) return ps[j].TypeFullName;
        return null;
    }

    /// <summary>ノードの ctx 引数の名前。なければ null。</summary>
    private static string? CtxArgOf(NodeModel n)
    {
        if (n.Rules.Count == 0) return null;
        var ps = n.Rules[0].Parameters;
        for (int j = ps.Count - 1; j >= 0; j--)
            if (ps[j].IsContext) return ps[j].Name ?? "ctx";
        return null;
    }

    private static int EncodeAction(LrAction a)
    {
        int k = a.Kind switch { LrActionKind.Shift => 1, LrActionKind.Reduce => 2, LrActionKind.Accept => 3, _ => 0 };
        return k * 1000000 + (a.Value < 0 ? 0 : a.Value);
    }

    private static byte ActionKindByte(LrAction a) => a.Kind switch
    {
        LrActionKind.Shift => 1,
        LrActionKind.Reduce => 2,
        LrActionKind.Accept => 3,
        _ => 0
    };

    private static (string type, string lexer, string parser) Names(GrammarModel model)
    {
        var (ns, type) = CodeEmitter.SplitFullName(model.RootTypeFullName);
        var suffix = string.IsNullOrEmpty(model.Mode) ? "" : "_" + model.Mode;
        return (type + suffix, type + suffix + "Lexer", type + suffix + "Parser");
    }

    private static void EmitMatrixByte(StringBuilder sb, string name, LalrTable _, int rows, int cols,
        System.Func<int, System.Func<int, byte>> cell)
    {
        // 1次元配列に平坦化: index = state * SymbolCount + sym
        // 2次元配列 (byte[,]) は境界チェックが2回入るが、1次元配列 (byte[]) は1回で済む。
        sb.Append("    public static readonly byte[] ").Append(name).Append(" = new byte[").Append(rows * cols).AppendLine("]");
        sb.AppendLine("    {");
        for (int r = 0; r < rows; r++)
        {
            var row = cell(r);
            sb.Append("        ");
            for (int c = 0; c < cols; c++) { if (c > 0) sb.Append(", "); sb.Append(row(c)); }
            sb.AppendLine(r < rows - 1 ? "," : "");
        }
        sb.AppendLine("    };");
    }

    private static void EmitMatrixIntRaw(StringBuilder sb, string name, int rows, int cols,
        System.Func<int, System.Func<int, int>> cell)
    {
        sb.Append("    public static readonly int[] ").Append(name).Append(" = new int[").Append(rows * cols).AppendLine("]");
        sb.AppendLine("    {");
        for (int r = 0; r < rows; r++)
        {
            var row = cell(r);
            sb.Append("        ");
            for (int c = 0; c < cols; c++) { if (c > 0) sb.Append(", "); sb.Append(row(c)); }
            sb.AppendLine(r < rows - 1 ? "," : "");
        }
        sb.AppendLine("    };");
    }
